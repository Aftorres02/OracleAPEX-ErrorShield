declare
  l_count pls_integer;
begin
  select count(1)
    into l_count
    from user_tables
   where table_name = 'ERSH_CONSTRAINT_LOOKUP';

  if l_count = 0 then
    execute immediate q'!
      create table ersh_constraint_lookup (
          ersh_constraint_lookup_id  number generated by default on null as identity (start with 1) primary key not null
        , constraint_name            varchar2(100 char) not null
        , constraint_message         varchar2(500 char) not null
        , created_by                 varchar2(60 char) default
                                       coalesce(
                                         sys_context('APEX$SESSION', 'app_user')
                                         , regexp_substr(sys_context('userenv', 'client_identifier'), '^[^:]*')
                                         , sys_context('userenv', 'session_user')
                                       )
                                       not null
        , created_on                 timestamp with local time zone default localtimestamp not null
        , last_updated_by            varchar2(60 char)
        , last_updated_on            timestamp with local time zone
      )
    !';
  end if;
end;
/


-- -----------------------------------------------------------------------------
-- Constraints
-- -----------------------------------------------------------------------------
declare
  l_count pls_integer;
begin
  select count(1)
    into l_count
    from user_constraints
   where constraint_name = 'UK_ERSH_CONSTRAINT_LOOKUP_NAME'
     and table_name = 'ERSH_CONSTRAINT_LOOKUP';

  if l_count = 0 then
    execute immediate '
      alter table ersh_constraint_lookup
        add constraint uk_ersh_constraint_lookup_name unique (constraint_name)
    ';
  end if;
end;
/


-- -----------------------------------------------------------------------------
-- Trigger
-- -----------------------------------------------------------------------------
create or replace trigger ersh_constraint_lookup_trg
  for insert or update on ersh_constraint_lookup
  compound trigger

  before each row is
  begin
    if updating then
      :new.last_updated_on := localtimestamp;
      :new.last_updated_by := coalesce(
        sys_context('APEX$SESSION', 'app_user')
        , regexp_substr(sys_context('userenv', 'client_identifier'), '^[^:]*')
        , sys_context('userenv', 'session_user')
      );
    end if;
  end before each row;

end ersh_constraint_lookup_trg;
/


-- -----------------------------------------------------------------------------
-- Comments
