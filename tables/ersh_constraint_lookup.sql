begin
  execute immediate 'comment on column ersh_constraint_lookup.ersh_constraint_lookup_id is ''Unique identifier for the constraint (Primary Key).''';
  execute immediate 'comment on column ersh_constraint_lookup.constraint_name is ''Name of the constraint.''';
  execute immediate 'comment on column ersh_constraint_lookup.constraint_message is ''Message for the constraint.''';
  execute immediate 'comment on column ersh_constraint_lookup.created_by is ''User who created the record.''';
  execute immediate 'comment on column ersh_constraint_lookup.created_on is ''Timestamp when the record was created.''';
  execute immediate 'comment on column ersh_constraint_lookup.last_updated_by is ''User who last updated the record.''';
  execute immediate 'comment on column ersh_constraint_lookup.last_updated_on is ''Timestamp when the record was last updated.''';
  execute immediate 'comment on table ersh_constraint_lookup is ''Lookup table for constraints used in the error handling package.''';
end;
